package day5;

public class Car {
    private int issue;
    private String color;
    private String model;

    public void setIssue(int CarIssue) {
        issue = CarIssue;
    }

    public int getIssue() {
        return issue;
    }

    public void setColor(String CarColor) {
        color = CarColor;
    }

    public String getColor() {
        return color;
    }

    public void setModel(String CarModel) {
        model = CarModel;
    }

    public String getModel() {
        return model;
    }
}
